function getLocalisation(text)
	if nameToLocalization and nameToLocalization[text] then
		return nameToLocalization[text]
	end
	return text
end

function getColorFromName(color)
	if nameToColor and nameToColor[color] then
		return nameToColor[color]
	end
	return nameToColor["nobody"]
end

function getRandomPedModel()
	return possiblePeds[math.random(1, #possibleVehicles)]
end

function getRandomVehicleModel()
	return possibleVehicles[math.random(1, #possibleVehicles)]
end

function convertTime(time)
	local minutes = math.floor(math.modf(time,3600)/60)
	local seconds = math.floor(time%60)
	return string.format("%02d:%02d",minutes,seconds)
end
